#include <unistd.h>

int ft_putchar (char n)
{
	write(1, &n, 1);
	return (0);
}

void ft_is_negative(int n)
{
	
	if(n >= 0)
	{
		write(1, "P", 1);
	}
	else
	 { 
		 write(1, "N", 1);
	 }
}


